class TabletManager;

// Whether eager PK-index SST build is supported for `schema`. Encapsulates the primary-key-encoding (and
// separate-sort-key opt-in) preconditions shared by eager build enablement
// (TabletWriter::try_enable_pk_index_eager_build) and separate-sort-key load spill
// (DeltaWriterImpl::should_enable_load_spill), which is only correct when the unsort SST writer runs to
// resolve duplicate primary keys. Needs no tablet metadata: shared-data PK tables are always cloud-native
// (the only index eager build supports), so the decision is a pure function of the schema. Eager build is
// always enabled for a supported schema -- there is no config gate (it must stay on for correctness on the
// separate-sort-key path, which resolves non-adjacent duplicate primary keys only via the unsort writer).
bool pk_index_eager_build_supported(const TabletSchema& schema);

// Whether a load into `schema` must preserve in-transaction upsert/delete order. True when the mutable
// config lake_enable_pk_preserve_txn_delete_order is on, OR when the separate-sort-key load-spill
// optimization is enabled for a separate-sort-key table: that path's unsort SST writer resolves
// duplicate primary keys by flush order (last-op-wins), so it ALWAYS needs the op-aware merge and the
// serialized del_op_offsets, regardless of the preserve config -- otherwise a DELETE and a later re-UPSERT
// of the same key that split across parallel merge tasks would resolve inconsistently. Snapshot the
// result once per load (both configs are mutable).
bool pk_preserve_txn_delete_order_enabled(const TabletSchema& schema);

enum WriterType : int { kHorizontal = 0, kVertical = 1 };

// Sentinel for a del file whose in-transaction op_offset (segment index it follows) is unknown,
// e.g. when produced by the spill/concurrent flush path where flush order does not map to the
// final merged segment indices. The persist path treats this as "after all segments" (max segment
// index), preserving the legacy behavior.
static constexpr uint32_t kUnknownDelOpOffset = std::numeric_limits<uint32_t>::max();

// Basic interface for tablet writers.
class TabletWriter {
public:
    explicit TabletWriter(TabletManager* tablet_mgr, int64_t tablet_id, std::shared_ptr<const TabletSchema> schema,
                          int64_t txn_id, bool is_compaction, ThreadPool* flush_pool = nullptr)
            : _tablet_mgr(tablet_mgr),
              _tablet_id(tablet_id),
              _schema(std::move(schema)),
              _txn_id(txn_id),
              _flush_pool(flush_pool),
              _is_compaction(is_compaction) {}

    virtual ~TabletWriter() = default;

    TabletManager* tablet_manager() const { return _tablet_mgr; }

    int64_t tablet_id() const { return _tablet_id; }

    int64_t txn_id() const { return _txn_id; }

    // Return a list of file info generated by this writer.
    // Note: The path of `FileInfo` is only base filename, no directory component.
    //
    // PREREQUISITES: the writer has successfully `finish()`ed but not yet `close()`ed.
    const std::vector<SegmentFileInfo>& segments() const { return _segments; }

    const std::vector<FileInfo>& dels() const { return _dels; }

    // Parallel to dels(): the in-transaction op_offset for each del file (segment index it
    // logically follows), or kUnknownDelOpOffset when the writer cannot determine the order.
    const std::vector<uint32_t>& del_op_offsets() const { return _del_op_offsets; }

    const std::vector<FileInfo>& ssts() const { return _ssts; }

    const std::vector<PersistentIndexSstableRangePB>& sst_ranges() const { return _sst_ranges; }

    // Parallel to ssts(): per-segment rowids that lost primary-key dedup inside the unsort SST
    // writer and must be masked by a delete vector at publish. Empty vectors for segments/paths
    // that produced no such losers (e.g. the sort-key==PK eager path). PK table only.
    const std::vector<std::vector<uint32_t>>& seg_delvecs() const { return _seg_delvecs; }

    // The sum of all segment file sizes, in bytes.
    int64_t data_size() const { return _data_size; }

    // The total number of rows have been written.
    int64_t num_rows() const { return _num_rows; }

    // This method is called immediately before any elements are processed, it
    // should contain the writer's initialization logic.
    virtual Status open() = 0;

    // Writes elements from the specified chunk to this rowset.
    //
    // It's guaranteed that the elements in each chunk are arranged in ascending
    // order, and the elements among all chunks written before `flush()` are also
    // arranged in ascending order.
    //
    // For horizontal writer.
    virtual Status write(const Chunk& data, SegmentPB* segment = nullptr, bool eos = false) = 0;

    // Writes both chunk and each rows's rssid & rowid
    // For horizontal writer.
    virtual Status write(const Chunk& data, const std::vector<uint64_t>& rssid_rowids,
                         SegmentPB* segment = nullptr) = 0;

    // Single-flush direct-write fast path (no __op column). The whole load is a single flush, which a
    // lake PK memtable already deduplicated by primary key, so no cross-flush merge/dedup is needed. For
    // a PK writer this writes a plain (sort-key-ordered) segment and SKIPS the eager PK-index SST, so
    // publish rebuilds the index lazily via the order-independent parallel_upsert -- avoiding the
    // spill+merge round trip and, for separate-sort-key eager writers, the unsort SST writer's per-row
    // order-key requirement. Default (non-PK, or writers with no eager SST): same as write().
    virtual Status write_single_flush(const Chunk& data, SegmentPB* segment, bool eos) {
        return write(data, segment, eos);
    }

    // Same single-flush fast path for a load whose chunk still carries the trailing __op column (the
    // op-aware path). Splits __op: upserts go through write_single_flush() (plain segment, eager SST
    // skipped), deletes are encoded to a del file. A single flush is unique-by-PK, so each key has
    // exactly one op and the split is unambiguous. Only the horizontal PK writer implements it.
    virtual Status write_single_flush_with_op(const Chunk& chunk_with_op, SegmentPB* segment, bool eos) {
        return Status::NotSupported("write_single_flush_with_op not implemented");
    }

    // Feeds the batch's DELETE rows (with their per-row ordering key) to the eager PK-index SST
    // writer so it can resolve the latest op per primary key (op-aware separate-sort-key spill path).
    // Unlike write(), these rows are NOT written to a segment. Default no-op: only the PK writer with
    // the unsort SST writer consumes them; other writers receive deletes via the caller's del file.
    virtual Status append_pk_index_deletes(const Chunk& data, const std::vector<uint64_t>& rssid_rowids) {
        return Status::OK();
    }

    // Writes partial columns data to this rowset.
    //
    // It's guaranteed that the elements in each chunk are arranged in ascending order by keys,
    // and multiple segments will be automatically generated according to `max_rows_per_segment`.
    //
    // For vertical writer.
    virtual Status write_columns(const Chunk& data, const std::vector<uint32_t>& column_indexes, bool is_key) = 0;

    // Writes partial columns data and each rows's rssid & rowid to this rowset.
    //
    // For vertical writer.
    virtual Status write_columns(const Chunk& data, const std::vector<uint32_t>& column_indexes, bool is_key,
                                 const std::vector<uint64_t>& rssid_rowids) = 0;

    // Write del file to this rowset. PK table only.
    // |op_offset| is the in-transaction segment index this delete logically follows
    // (delete rssid = rowset_id + op_offset); pass kUnknownDelOpOffset when the order is unknown.
    virtual Status flush_del_file(const Column& deletes, uint32_t op_offset) = 0;

    // Flushes this writer and forces any buffered bytes to be written out to segment files.
    // There is no order guarantee between the data written before a `flush()`
    // and the data written after it.
    virtual Status flush(SegmentPB* segment = nullptr) = 0;

    // Flushes partial columns data when current columns are written finished.
    //
    // For vertical writer.
    virtual Status flush_columns() = 0;

    // This method is called at the end of data processing.
    virtual Status finish(SegmentPB* segment = nullptr) = 0;

    // This method is called at the very end of the operator's life, both in
    // the case of a successful completion of the operation, and in the case
    // of a failure and canceling.
    virtual void close() = 0;

    //  return the rowset txn meta generated by tablet writer
    virtual RowsetTxnMetaPB* rowset_txn_meta() = 0;

    // The clone function is intended to duplicate writers with similar configurations for
    // use in parallel segment generation and writing.
    virtual StatusOr<std::unique_ptr<TabletWriter>> clone() const { return Status::NotSupported("Not implemented"); }

    // Once all cloned writers have completed segment file generation, their outputs are merged into a single writer
    // using the merge_other_writers function for further processing.
    // E.g.
    //      for (i ... n) {
    //          cloned_writers[i]->write(...)
    //          cloned_writers[i]->finish()
    //      }
    //      main_writer->merge_other_writers(cloned_writers)
    Status merge_other_writers(const std::vector<std::unique_ptr<TabletWriter>>& other_writers);
    Status merge_other_writer(const TabletWriter* other_writer);

    // allow to set custom tablet schema for writer, used in partial update
    void set_tablet_schema(TabletSchemaCSPtr schema) { _schema = std::move(schema); }

    const starrocks::TabletSchemaCSPtr& tablet_schema() const { return _schema; }

    void set_auto_flush(bool auto_flush) { _auto_flush = auto_flush; }

    bool auto_flush() const { return _auto_flush; }

    void set_fs(std::shared_ptr<FileSystem> fs) { _fs = std::move(fs); }
    void set_location_provider(std::shared_ptr<LocationProvider> location_provider) {
        _location_provider = std::move(location_provider);
    }

    const OlapWriterStatistics& stats() const { return _stats; }

    const DictColumnsValidMap& global_dict_columns_valid_info() const { return _global_dict_columns_valid_info; }

    // When the system determines that eager PK index build can be enabled
    // (for example, during large imports or major compaction tasks), it will invoke this function.
    // However, whether eager PK index build is actually enabled still depends on the schema.
    void try_enable_pk_index_eager_build();

    bool enable_pk_index_eager_build() const { return _enable_pk_index_eager_build; }
    void force_set_enable_pk_index_eager_build() { _enable_pk_index_eager_build = true; }

    // Set only on the schema-change conversion writer for the ALTER's shadow tablet, so an
    // async-mode ADD still builds existing data's .vi during the rewrite. Other writers: false.
    bool force_build_vector_index_inline() const { return _force_build_vector_index_inline; }
    void force_set_build_vector_index_inline() { _force_build_vector_index_inline = true; }

    // Flat JSON policy delivered with the load plan (shared-data). When set, the segment writer
    // uses it to decide JSON flattening instead of reading the best-effort tablet-metadata cache.
    const std::shared_ptr<FlatJsonConfig>& flat_json_config() const { return _flat_json_config; }
    void set_flat_json_config(std::shared_ptr<FlatJsonConfig> flat_json_config) {
        _flat_json_config = std::move(flat_json_config);
    }

    void check_global_dict(SegmentWriter* segment_writer);

    const FileInfo& lcrm_file() const { return _lcrm_file; }

protected:
    TabletManager* _tablet_mgr;
    int64_t _tablet_id;
    TabletSchemaCSPtr _schema;
    int64_t _txn_id;
    ThreadPool* _flush_pool;
    std::vector<SegmentFileInfo> _segments;
    std::vector<FileInfo> _dels;
    // Parallel to _dels: op_offset (segment index the delete follows) for each del file.
    std::vector<uint32_t> _del_op_offsets;
    std::mutex _dels_mutex;
    std::vector<FileInfo> _ssts;
    std::vector<PersistentIndexSstableRangePB> _sst_ranges;
    // Parallel to _ssts: dedup-loser rowids per segment (see seg_delvecs()).
    std::vector<std::vector<uint32_t>> _seg_delvecs;
    // lake compaction row mapper file
    FileInfo _lcrm_file;
    int64_t _num_rows = 0;
    int64_t _data_size = 0;
    uint32_t _seg_id = 0;
    bool _finished = false;
    bool _auto_flush = true;
    std::shared_ptr<FileSystem> _fs;
    std::shared_ptr<LocationProvider> _location_provider;
    OlapWriterStatistics _stats;

    bool _is_compaction = false;
    DictColumnsValidMap _global_dict_columns_valid_info;
    bool _enable_pk_index_eager_build = false;
    bool _force_build_vector_index_inline = false;
    std::shared_ptr<FlatJsonConfig> _flat_json_config;
};

